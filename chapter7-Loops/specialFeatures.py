#--------------------------------------------------------------
#      USE of Break statement with for loop 
#--------------------------------------------------------------
'''
--> Break basically breaks the execution of the loop when the condition
    gets true
'''

'''
for i in range(0,11):
    if(i == 5):
      break 
    print(i)        #OUTPUT : 0,1,2,3,4
'''

#--------------------------------------------------------------
#      USE of Continue statement with for loop 
#--------------------------------------------------------------
'''
--> This basically skips the iteration when the condition gets true.
'''


'''
for i in range(0,11):
    if(i == 5):
      continue 
    print(i)        #OUTPUT : 0,1,2,3,4,6,7,8,9,10

'''
